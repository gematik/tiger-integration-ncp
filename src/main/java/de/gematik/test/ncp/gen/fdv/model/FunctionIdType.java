package de.gematik.test.ncp.gen.fdv.model;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonValue;
import jakarta.validation.constraints.*;

/** function id of consent related functions of a health record. */
public enum FunctionIdType {
  ERP_SUBMISSION("erp-submission"),

  RESEARCH_SUBMISSION("research-submission"),

  MEDICATION("medication");

  private String value;

  FunctionIdType(String value) {
    this.value = value;
  }

  @Override
  @JsonValue
  public String toString() {
    return String.valueOf(value);
  }

  @JsonCreator
  public static FunctionIdType fromValue(String value) {
    for (FunctionIdType b : FunctionIdType.values()) {
      if (b.value.equals(value)) {
        return b;
      }
    }
    throw new IllegalArgumentException("Unexpected value '" + value + "'");
  }
}
